# Prograna que calcule el área de un circulo, un rectangulo y de un triángulo

# area del circulo
radio = 4
area_circulo = 3.14 * radio ** 2

# area del rectangulo
L1 = 5
L2 = 2

area_rectangulo = L1 * L2
B = 10
H = 12

# area del triángulo
area_triangulo = (B * H) / 2

print(f"Area circulo {area_circulo}")
print(f"Area rectangulo {area_rectangulo}")
print(f"Area triangulo {area_triangulo}")
